public class MediaDeNotas {
    public static void main(String[] args) {

        double nota1 = 9;

        double nota2 = 9;

        double nota3 = 7;

        double nota4 = 5;

        double somaDasNotas = nota1 + nota2 + nota3 + nota4;

        double calculoMediaNotas = somaDasNotas / 4;

        System.out.println("A soma das notas: " + somaDasNotas);
        System.out.println("A media das notas: " + calculoMediaNotas);
    }
}
